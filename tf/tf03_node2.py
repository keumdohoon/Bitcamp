# 3 + 4 + 5
# 4 - 3
# 3 * 4
# 4 / 2


import tensorflow as tf

node1 = tf.constant(3.0, tf.float32)
node2 = tf.constant(4.0)
node3 = tf.constant(5.0)

# node4 = tf.add(node1, node2, node3)

node4=tf.add_n([node1, node2,node3])
print("node1:", node1, "node2 : ", node2, "node3 :", node3)
print("node3 :", node4)
#두개이상의 덧셈 식을 할때에는 무조건 _n을 붙여줘야지 제대로 연산이 된다. 



sess = tf.Session()
print("sess.run(node1, node2, node3) : ", sess.run([node1, node2, node3]))
print("sess.run(node4) : ", sess.run(node4))


node5 = tf.multiply(
    node1, node2, name=None
)
print("sess.run(node4) : ", sess.run(node5))



node6 = tf.subtract(
    node2, node1, name=None
)
print("sess.run(node4) : ", sess.run(node6))



node7 = tf.divide(
    node2, node1, name=None
)
print("sess.run(node4) : ", sess.run(node7))

